package com.xworkz.Variables;

public class VariableTypes {

	//instance variable
	int id;
	String name;
	
	//static variable
	static String brand;
	
	//instance method
	void instanceMethod() {
		System.out.println("i'm  instance method");
	}
	
	//static method
	static void staticMethod() {
		System.out.println("i'm static method");
		
	}
	
}
